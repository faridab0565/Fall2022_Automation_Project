package Day3_10082022;

public class T1_If_Else_Condition {
    public static void main(String[] args) {
        //declaring two integer variables for if else condition
        int a, b;
        a = 3;
        b = 3;
        //verify if a is less than b or not
        if (a < b) {
            System.out.println("a is less than b"); //this will not print since it is not true
        } else {
            System.out.println("a is either greater or equal to b");
        }//end of if else condition

    }//end of main


}//end of java class
